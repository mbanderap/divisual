-- ---------------------------------------------------------------------
-- PASO 4 — Row Level Security: acceso completo para cualquier usuario
-- autenticado en todas las tablas (mismo criterio en toda la app; no
-- hay registro público, así que no hace falta granularidad por filas).
-- ---------------------------------------------------------------------

do $$
declare
  t text;
begin
  foreach t in array array[
    'personnel', 'companies', 'contacts', 'tags', 'billing_models', 'hotels',
    'stories', 'sprints', 'labels', 'events',
    'contacts_companies', 'contacts_historial', 'personnel_historial',
    'tags_contacts', 'hotels_personnel', 'events_personnel',
    'deals', 'deals_hotels', 'tickets', 'tickets_contacts',
    'tasks', 'tasks_personnel', 'task_comments', 'task_subitems',
    'task_attachments', 'tasks_labels', 'messages'
  ]
  loop
    execute format('alter table %I enable row level security;', t);
    execute format(
      'drop policy if exists "authenticated full access" on %I;', t
    );
    execute format(
      'create policy "authenticated full access" on %I for all to authenticated using (true) with check (true);', t
    );
  end loop;
end $$;


