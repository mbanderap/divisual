-- Guarda cuándo cambia la fecha de cierre de un negocio (para el KPI
-- "Cambios en la fecha" del Panel).
create or replace function deals_track_closing_date_change() returns trigger as $$
begin
  if new.closing_date is distinct from old.closing_date then
    new.closing_date_prev := old.closing_date;
    new.closing_date_changed_at := now();
  end if;
  return new;
end;
$$ language plpgsql;

drop trigger if exists trg_deals_closing_date_change on deals;
create trigger trg_deals_closing_date_change
  before update on deals
  for each row execute function deals_track_closing_date_change();

-- Guarda cuándo cambia la etapa de un negocio (para detectar negocios
-- "estancados": mucho tiempo sin moverse de etapa).
create or replace function deals_track_status_change() returns trigger as $$
begin
  if new.status is distinct from old.status then
    new.status_prev := old.status;
    new.status_changed_at := now();
  end if;
  return new;
end;
$$ language plpgsql;

drop trigger if exists trg_deals_status_change on deals;
create trigger trg_deals_status_change
  before update on deals
  for each row execute function deals_track_status_change();

-- Reconstrucción: mantiene contacts.last_update al día en cada cambio
-- (la app solo lee esta columna; nunca la escribe a mano).
create or replace function contacts_touch_last_update() returns trigger as $$
begin
  new.last_update := now();
  return new;
end;
$$ language plpgsql;

drop trigger if exists trg_contacts_last_update on contacts;
create trigger trg_contacts_last_update
  before update on contacts
  for each row execute function contacts_touch_last_update();


