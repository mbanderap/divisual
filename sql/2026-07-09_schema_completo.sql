-- PASO 6 — Realtime: para que el Chat y las notificaciones de tareas
-- asignadas lleguen al instante, sin recargar la página.
-- ---------------------------------------------------------------------

do $$
begin
  if not exists (
    select 1 from pg_publication_tables
    where pubname = 'supabase_realtime' and schemaname = 'public' and tablename = 'messages'
  ) then
    alter publication supabase_realtime add table messages;
  end if;
  if not exists (
    select 1 from pg_publication_tables
    where pubname = 'supabase_realtime' and schemaname = 'public' and tablename = 'tasks_personnel'
  ) then
    alter publication supabase_realtime add table tasks_personnel;
  end if;
end $$;


